float my_sinf(float x) {
    int index;
    float phase;
    phase = x;
    while (phase < 0) phase += TWO_PI;
    while (phase >= TWO_PI) phase -= TWO_PI;
    index = (int)((phase / TWO_PI) * SIN_LUT_SIZEF);
    return sin_lut[index];
}

float my_cosf(float x) {
    return my_sinf(x + HALFPI);
}

