int32 projectPoint(Vec3 world_pt, const Camera *cam, float cosY, float sinY, float cosP, float sinP, int16 *sx, int16 *sy, int16 *sz) {
    float x = world_pt.x - cam->pos.x;
    float y = world_pt.y - cam->pos.y;
    float z = world_pt.z - cam->pos.z;
    float rx = x * cosY + z * sinY;
    float rz = -x * sinY + z * cosY;
    float final_x = rx;
    float final_y = y * cosP - rz * sinP;
    float final_z = y * sinP + rz * cosP;
    float invZ;

    if (final_z < NEAR_Z) {
        return MR_FAILED;
    }

    *sz = (int16)final_z;
    invZ = FOCAL_LENGTH / final_z;

    *sx = (int16)(final_x * invZ + SCREEN_WIDTH2F);
    *sy = (int16)(final_y * invZ + SCREEN_HEIGHT2F);

    return MR_SUCCESS;
}

/* Same projection as projectPoint(), but the inputs are pre-converted Q16.16
   fixed-point values.  Does no software-float work, which is the main hot
   loop on no-FPU ARM926EJ-S cores. */
int32 projectPoint_fx(fx32 wx, fx32 wy, fx32 wz,
                      fx32 cam_x, fx32 cam_y, fx32 cam_z,
                      fx32 cosY, fx32 sinY, fx32 cosP, fx32 sinP,
                      int16 *sx, int16 *sy, int16 *sz) {
    fx32 x  = wx - cam_x;
    fx32 y  = wy - cam_y;
    fx32 z  = wz - cam_z;
    fx32 rx = FX_MUL(x, cosY) + FX_MUL(z, sinY);
    fx32 rz = -FX_MUL(x, sinY) + FX_MUL(z, cosY);
    fx32 final_x = rx;
    fx32 final_y = FX_MUL(y, cosP) - FX_MUL(rz, sinP);
    fx32 final_z = FX_MUL(y, sinP) + FX_MUL(rz, cosP);
    fx32 invZ;

    if (final_z < FX_NEAR_Z) {
        return MR_FAILED;
    }

    /* sz: integer depth, just used for ordering / debugging. */
    *sz = (int16)FX_TO_INT(final_z);

    /* invZ = FX_FOCAL / final_z, also Q16.16. */
    invZ = FX_DIV(FX_FOCAL, final_z);

    /* screen_x = final_x * invZ + half_width.  Result is Q16.16, then
       integer-truncated for an int16 pixel coordinate. */
    *sx = (int16)FX_TO_INT(FX_MUL(final_x, invZ) + FX_SCREEN_HW);
    *sy = (int16)FX_TO_INT(FX_MUL(final_y, invZ) + FX_SCREEN_HH);

    return MR_SUCCESS;
}

/* ===== Tiny 3x5 bitmap font (digits + 'F','P','S',':',' ') =====
   Each row is 3 bits stored in the low nibble; row 0 is the top.
   We only need the chars used by the FPS HUD so the table is small. */
static const uint8 _font3x5[16][5] = {
    /* 0 */ { 0x7,0x5,0x5,0x5,0x7 },
    /* 1 */ { 0x2,0x6,0x2,0x2,0x7 },
    /* 2 */ { 0x6,0x1,0x2,0x4,0x7 },
    /* 3 */ { 0x6,0x1,0x2,0x1,0x6 },
    /* 4 */ { 0x5,0x5,0x7,0x1,0x1 },
    /* 5 */ { 0x7,0x4,0x6,0x1,0x6 },
    /* 6 */ { 0x3,0x4,0x6,0x5,0x2 },
    /* 7 */ { 0x7,0x1,0x2,0x4,0x4 },
    /* 8 */ { 0x2,0x5,0x2,0x5,0x2 },
    /* 9 */ { 0x2,0x5,0x3,0x1,0x6 },
    /* F */ { 0x7,0x4,0x6,0x4,0x4 },
    /* P */ { 0x6,0x5,0x6,0x4,0x4 },
    /* S */ { 0x3,0x4,0x2,0x1,0x6 },
    /* : */ { 0x0,0x2,0x0,0x2,0x0 },
    /* (sp)*/{ 0x0,0x0,0x0,0x0,0x0 },
    /* . */ { 0x0,0x0,0x0,0x0,0x2 }
};
/* Map a logical char to a glyph index in _font3x5. */
static int _font_glyph_index(char c)
{
    if (c >= '0' && c <= '9') return c - '0';
    if (c == 'F') return 10;
    if (c == 'P') return 11;
    if (c == 'S') return 12;
    if (c == ':') return 13;
    if (c == '.') return 15;
    return 14; /* space / unknown */
}
/* Draw one 3x5 glyph at (x,y) scaled by `scale` (1 = 3x5 px,
   2 = 6x10, etc). Pixels are emitted as horizontal mrc_drawLine
   runs of width `scale` so a scale=2 char is just 5 line draws. */
static void drawGlyph5(int x, int y, int glyph, int scale,
                       uint8 r, uint8 g, uint8 b)
{
    int row, col, dy, px_x, px_y;
    if (glyph < 0 || glyph >= 16 || scale <= 0) return;
    for (row = 0; row < 5; row++) {
        uint8 bits = _font3x5[glyph][row];
        for (col = 0; col < 3; col++) {
            if (bits & (1 << (2 - col))) {
                px_x = x + col * scale;
                px_y = y + row * scale;
                for (dy = 0; dy < scale; dy++) {
                    mrc_drawLine((int16)px_x, (int16)(px_y + dy),
                                 (int16)(px_x + scale - 1), (int16)(px_y + dy),
                                 r, g, b);
                }
            }
        }
    }
}
/* Draw a NUL-terminated ASCII string left-to-right at (x,y). Each
   char is 3*scale wide with a 1*scale gap, total advance = 4*scale. */
static void drawString5(int x, int y, const char *s, int scale,
                        uint8 r, uint8 g, uint8 b)
{
    int cx = x;
    while (*s) {
        drawGlyph5(cx, y, _font_glyph_index(*s), scale, r, g, b);
        cx += 4 * scale;
        s++;
    }
}
/* Timer tick period -- must match mrc_timerStart() in mrc_init().
   Used by the FPS counter's tick-based fallback when mr_getTime()
   does not advance (some MAUI builds stub it to 0). */
#define FRAME_TICK_MS 33
/* mr_getTime() returns elapsed milliseconds since app start (MRE SDK).
   The SDL2 test harness implements its own version. */
extern int32 mr_getTime(void);
/* Update + draw the FPS counter at top-left.
   Two-source clock: wall-clock from mr_getTime() if it advances,
   otherwise fall back to a synthetic clock built from the timer
   tick rate (FRAME_TICK_MS per call). The fallback always works
   even on platforms where mr_getTime() returns 0/static. */
void drawFpsHud(void)
{
    char buf[16];
    int n;
    int32 now = mr_getTime();
    int elapsed;
    g_fps_window_frames++;
    if (!g_fps_inited) {
        g_fps_window_start_ms = now;
        g_fps_window_frames = 0;
        g_fps_inited = 1;
    }
    elapsed = (int)(now - g_fps_window_start_ms);
    /* If the wall clock is not advancing (mr_getTime stuck at 0),
       synthesise it from the tick count so the HUD still updates. */
    if (elapsed <= 0)
        elapsed = g_fps_window_frames * FRAME_TICK_MS;
    if (elapsed >= 1000) {
        g_fps = (g_fps_window_frames * 1000) / elapsed;
        g_fps_window_start_ms = now;
        g_fps_window_frames = 0;
    }
    /* Build "FPS:NN" or "FPS:NNN" inline (no sprintf to keep MRE
       happy on devices without full libc). */
    buf[0] = 'F'; buf[1] = 'P'; buf[2] = 'S'; buf[3] = ':';
    n = g_fps;
    if (n < 0) n = 0;
    if (n > 999) n = 999;
    if (n >= 100) {
        buf[4] = (char)('0' + (n / 100));
        buf[5] = (char)('0' + ((n / 10) % 10));
        buf[6] = (char)('0' + (n % 10));
        buf[7] = '\0';
    } else if (n >= 10) {
        buf[4] = (char)('0' + (n / 10));
        buf[5] = (char)('0' + (n % 10));
        buf[6] = '\0';
    } else {
        buf[4] = (char)('0' + n);
        buf[5] = '\0';
    }
    /* Drop a 1-px black drop-shadow then bright yellow text. */
    drawString5(5, 5, buf, 2,   0,   0,   0);
    drawString5(4, 4, buf, 2, 255, 220,  40);
}
/* ===== Raycast block break / place =====
   Step a ray from cam.pos along the look direction in small fixed
   increments until either we hit a solid block or run past PLAYER_REACH.
   On hit:
     action == 0 (break): set the hit cell to AIR
     action == 1 (place): set the LAST AIR cell on the ray to DIRT
   In both cases we incrementally rebuild the affected face masks
   via updateFaceMaskRegion(). */
void castRayAndAct(int action)
{
    /* Direction matches the engine's yaw/pitch convention
       (pitch positive = look down, in screen-Y-down world). */
    float cosY = my_cosf(cam.yaw);
    float sinY = my_sinf(cam.yaw);
    float cosP = my_cosf(cam.pitch);
    float sinP = my_sinf(cam.pitch);
    float dirX =  sinY * cosP;
    float dirY =  sinP;
    float dirZ =  cosY * cosP;
    float t;
    int x, y, z;
    int prevX = -999, prevY = -999, prevZ = -999;
    for (t = 0.0f; t < PLAYER_REACH; t += RAY_STEP) {
        x = (int)(cam.pos.x + dirX * t);
        y = (int)(cam.pos.y + dirY * t);
        z = (int)(cam.pos.z + dirZ * t);
        if (x < 0 || x >= WORLD_SX ||
            y < 0 || y >= WORLD_SY ||
            z < 0 || z >= WORLD_SZ) {
            prevX = x; prevY = y; prevZ = z;
            continue;
        }
        if (world[x][y][z] != (uint8)BLOCK_AIR) {
            if (action == 0) {
                world[x][y][z] = (uint8)BLOCK_AIR;
                updateFaceMaskRegion(x, y, z);
            } else if (prevX >= 0 && prevX < WORLD_SX &&
                       prevY >= 0 && prevY < WORLD_SY &&
                       prevZ >= 0 && prevZ < WORLD_SZ) {
                /* Place a DIRT block in the last AIR cell on the ray. */
                world[prevX][prevY][prevZ] = (uint8)BLOCK_DIRT;
                updateFaceMaskRegion(prevX, prevY, prevZ);
            }
            return;
        }
        prevX = x; prevY = y; prevZ = z;
    }
}

int32 mrc_event(int32 ev, int32 p0, int32 p1) {
    if (ev == MR_KEY_PRESS) {
        dx = 0.0f;
        dz = 0.0f;
        if (p0 ==  MR_KEY_UP) {
        dx += my_sinf(cam.yaw) * move_speed;
        dz += my_cosf(cam.yaw) * move_speed;
        }
        if (p0 == MR_KEY_DOWN) {
        dx -= my_sinf(cam.yaw) * move_speed;
        dz -= my_cosf(cam.yaw) * move_speed;
        }
        if (p0 == MR_KEY_LEFT) {
        dx -= my_cosf(cam.yaw) * move_speed;
        dz += my_sinf(cam.yaw) * move_speed;
        }
        if (p0 == MR_KEY_RIGHT) {
        dx += my_cosf(cam.yaw) * move_speed;
        dz -= my_sinf(cam.yaw) * move_speed;
        }
        /* '5' breaks block at look-center, '0' places one in front.
           Both ASCII codes (53, 48) and MR_KEY_NUM5/0 (if defined by
           the SDK on this platform) are accepted. */
        if (p0 == 53) g_action_break = 1;
        if (p0 == 48) g_action_place = 1;
#ifdef MR_KEY_NUM5
        if (p0 == MR_KEY_NUM5) g_action_break = 1;
#endif
#ifdef MR_KEY_NUM0
        if (p0 == MR_KEY_NUM0) g_action_place = 1;
#endif

        cam.pos.x += dx;
        cam.pos.z += dz;
        
    } else if (ev == MR_MOUSE_DOWN) {
        g_curr_tx = p0;
        g_curr_ty = p1;
        diff_x = g_curr_tx - 120;
        diff_y = g_curr_ty - 160;
        
    } else if (ev == MR_MOUSE_UP) {
        diff_x = 0;
        diff_y = 0;
    }
    
    return MR_SUCCESS;
}

uint8 getBlock(int x, int y, int z) {
    if (x < 0 || x >= WORLD_SX) return (uint8)BLOCK_AIR;
    if (y < 0 || y >= WORLD_SY) return (uint8)BLOCK_AIR;
    if (z < 0 || z >= WORLD_SZ) return (uint8)BLOCK_AIR;
    return world[x][y][z];
}

uint8 computeFaceMask(int x, int y, int z) {
    int f;
    uint8 mask;
    if (world[x][y][z] == (uint8)BLOCK_AIR) return 0;
    mask = 0;
    for (f = 0; f < 6; f++) {
        if (getBlock(x + face_dx[f], y + face_dy[f], z + face_dz[f]) == (uint8)BLOCK_AIR) {
            mask = (uint8)(mask | (1 << f));
        }
    }
    return mask;
}

void rebuildAllFaceMasks(void) {
    int x, y, z;
    for (x = 0; x < WORLD_SX; x++) {
        for (y = 0; y < WORLD_SY; y++) {
            for (z = 0; z < WORLD_SZ; z++) {
                visibleFaces[x][y][z] = computeFaceMask(x, y, z);
            }
        }
    }
}

/* Recompute the face mask for (x,y,z) and its 6 axis neighbours.
   Call this after a single block changes (break / place). */
void updateFaceMaskRegion(int x, int y, int z) {
    int f, nx, ny, nz;
    if (x >= 0 && x < WORLD_SX && y >= 0 && y < WORLD_SY && z >= 0 && z < WORLD_SZ) {
        visibleFaces[x][y][z] = computeFaceMask(x, y, z);
    }
    for (f = 0; f < 6; f++) {
        nx = x + face_dx[f];
        ny = y + face_dy[f];
        nz = z + face_dz[f];
        if (nx >= 0 && nx < WORLD_SX && ny >= 0 && ny < WORLD_SY && nz >= 0 && nz < WORLD_SZ) {
            visibleFaces[nx][ny][nz] = computeFaceMask(nx, ny, nz);
        }
    }
}

void gameStart() {
    int x, y, z;
    cam.pos.x = (float)WORLD_SX / 2.0f;
    cam.pos.y = 2.0f;
    cam.pos.z = -2.0f;
    cam.yaw = 0.0f;
    cam.pitch = 0.0f;

    /* projectPoint() maps larger world Y to larger screen Y (i.e. lower
       on screen), so deeper underground sits at higher y indices and the
       camera sits at a low y just above the surface. */
    for (x = 0; x < WORLD_SX; x++) {
        for (y = 0; y < WORLD_SY; y++) {
            for (z = 0; z < WORLD_SZ; z++) {
                if (y == WORLD_SY - 1)                              world[x][y][z] = (uint8)BLOCK_BEDROCK;
                else if (y == WORLD_SY - 2 || y == WORLD_SY - 3)    world[x][y][z] = (uint8)BLOCK_STONE;
                else if (y == WORLD_SY - 4)                         world[x][y][z] = (uint8)BLOCK_DIRT;
                else if (y == WORLD_SY - 5)                         world[x][y][z] = (uint8)BLOCK_GRASS;
                else                                                world[x][y][z] = (uint8)BLOCK_AIR;
            }
        }
    }
}

void fillTriangle(int x0, int y0, int x1, int y1, int x2, int y2, uint8 r, uint8 g, uint8 b) {
    /* Объявление всех переменных в начале метода (C89) */
    int32 minY, maxY, y;
    int32 i, count;
    int32 ex_fp[3];      /* Текущие X координаты ребер в формате Fixed-point (Q16.16) */
    int32 edx_step[3];   /* Шаг изменения X на одну строку Y (Fixed-point) */
    int32 ey_start[3];   /* Начальная координата Y ребра */
    int32 ey_end[3];     /* Конечная координата Y ребра */
    int32 e_active[3];    /* Флаг: является ли ребро активным (не горизонтальным) */
    int32 ix[2];         /* Найденные пересечения X для текущей строки scanline */
    uint8 final_r, final_g, final_b; /* Локальные копии цветов */
    int32 joker;
    int32 temp_dy;
    int32 p0x, p0y, p1x, p1y, p2x, p2y;

    /* Подготовка данных */
    p0x = x0; p0y = y0;
    p1x = x1; p1y = y1;
    p2x = x2; p2y = y2;

    /* 1. Определяем границы отрисовки */
    minY = y0; if (y1 < minY) minY = y1; if (y2 < minY) minY = y2;
    maxY = y0; if (y1 > maxY) maxY = y1; if (y2 > maxY) maxY = y2;

    /* Если треугольник слишком плоский по вертикали, выходим */
    if (minY == maxY) return;

    /* Инициализация переменных ребер */
    for (i = 0; i < 3; i++) {
        e_active[i] = 0;
    }

    /* 2. Настройка трех ребер: (P0-P1), (P1-P2), (P2-P0) */
    /* Ребро 0: P0 -> P1 */
    temp_dy = p1y - p0y;
    if (temp_dy != 0) {
        e_active[0] = 1;
        if (p0y < p1y) {
            ey_start[0] = p0y; ey_end[0] = p1y;
            ex_fp[0] = p0x << 16;
            edx_step[0] = ((p1x - p0x) << 16) / temp_dy;
        } else {
            ey_start[0] = p1y; ey_end[0] = p0y;
            ex_fp[0] = p1x << 16;
            edx_step[0] = ((p0x - p1x) << 16) / (-temp_dy);
        }
    }

    /* Ребро 1: P1 -> P2 */
    temp_dy = p2y - p1y;
    if (temp_dy != 0) {
        e_active[1] = 1;
        if (p1y < p2y) {
            ey_start[1] = p1y; ey_end[1] = p2y;
            ex_fp[1] = p1x << 16;
            edx_step[1] = ((p2x - p1x) << 16) / temp_dy;
        } else {
            ey_start[1] = p2y; ey_end[1] = p1y;
            ex_fp[1] = p2x << 16;
            edx_step[1] = ((p1x - p2x) << 16) / (-temp_dy);
        }
    }

    /* Ребро 2: P2 -> P0 */
    temp_dy = p0y - p2y;
    if (temp_dy != 0) {
        e_active[2] = 1;
        if (p2y < p0y) {
            ey_start[2] = p2y; ey_end[2] = p0y;
            ex_fp[2] = p2x << 16;
            edx_step[2] = ((p0x - p2x) << 16) / temp_dy;
        } else {
            ey_start[2] = p0y; ey_end[2] = p2y;
            ex_fp[2] = p0x << 16;
            edx_step[2] = ((p2x - p0x) << 16) / (-temp_dy);
        }
    }

    /* 3. Основной цикл Scanline Renderer */
    for (y = minY; y <= maxY; y++) {
        count = 0;
        
        /* Ищем пересечения текущей строки Y с активными ребрами */
        for (i = 0; i < 3; i++) {
            if (e_active[i]) {
                /* Используем полуоткрытый интервал [start, end), чтобы избежать двойного учета вершин */
                if (y >= ey_start[i] && y < ey_end[i]) {
                    ix[count++] = ex_fp[i] >> 16; /* Перевод из Fixed-point обратно в int */
                    
                    /* Обновляем X для следующей строки сразу здесь (инкрементально) */
                    ex_fp[i] += edx_step[i];
                }
            }
        }

        /* Если нашли ровно два пересечения — это полноценная горизонтальная линия треугольника */
        if (count == 2) {
            /* Сортируем ix[0] и ix[1], чтобы leftX был меньше rightX */
            if (ix[0] > ix[1]) {
                long temp_x;
                temp_x = ix[0]; ix[0] = ix[1]; ix[1] = temp_x;
            }

            joker = (y % 16);
            final_r = r + joker; final_g = g + joker; final_b = b + joker;

            /* Рисуем линию через API библиотеки */
            mrc_drawLine((int16)ix[0], (int16)y, (int16)ix[1], (int16)y, final_r, final_g, final_b);
        }
    }
}
 
void drawCube(int32 fx, int32 fy, int32 fz, int type, uint8 faceMask,
              fx32 cosY_fx, fx32 sinY_fx, fx32 cosP_fx, fx32 sinP_fx,
              fx32 cam_x_fx, fx32 cam_y_fx, fx32 cam_z_fx) {
    int16 px[8], py[8], pz[8];
    uint8 valid[8];
    fx32  v_x[8], v_y[8], v_z[8];
    fx32  bx, by, bz;
    fx32  half = FX_HALF;
    uint8 r, g, b;
    int i, f;
    int16 v0, v1, v2, v3, sx, sy, sz;
    int32 edge1x, edge1y, edge2x, edge2y, normalZ;
    const int8* face;
    static const int8 faces[6][4] = {
        {0, 3, 2, 1}, {4, 5, 6, 7}, {0, 1, 5, 4},
        {3, 2, 6, 7}, {0, 4, 7, 3}, {1, 2, 6, 5}
    };
    if (type == BLOCK_DIRT) { r = 139; g = 69; b = 19; }
    else if (type == BLOCK_GRASS) { r = 150; g = 210; b = 150; }
    else if (type == BLOCK_STONE) { r = 128; g = 128; b = 128; }
    else if (type == BLOCK_BEDROCK) { r = 64; g = 64; b = 64; }
    else { r = 128; g = 128; b = 128; }

    bx = FX_FROM_INT(fx);
    by = FX_FROM_INT(fy);
    bz = FX_FROM_INT(fz);

    v_x[0] = bx - half; v_y[0] = by - half; v_z[0] = bz - half;
    v_x[1] = bx + half; v_y[1] = by - half; v_z[1] = bz - half;
    v_x[2] = bx + half; v_y[2] = by + half; v_z[2] = bz - half;
    v_x[3] = bx - half; v_y[3] = by + half; v_z[3] = bz - half;
    v_x[4] = bx - half; v_y[4] = by - half; v_z[4] = bz + half;
    v_x[5] = bx + half; v_y[5] = by - half; v_z[5] = bz + half;
    v_x[6] = bx + half; v_y[6] = by + half; v_z[6] = bz + half;
    v_x[7] = bx - half; v_y[7] = by + half; v_z[7] = bz + half;

    for (i = 0; i < 8; i++) {
        if (projectPoint_fx(v_x[i], v_y[i], v_z[i],
                            cam_x_fx, cam_y_fx, cam_z_fx,
                            cosY_fx, sinY_fx, cosP_fx, sinP_fx,
                            &sx, &sy, &sz) == MR_SUCCESS) {
            px[i] = sx; py[i] = sy; pz[i] = sz;
            valid[i] = 1;
        } else {
            px[i] = 0; py[i] = 0; pz[i] = 0;
            valid[i] = 0;
        }
    }

    for (f = 0; f < 6; f++) {
        if (!(faceMask & (1 << f))) continue;
        face = faces[f];
        v0 = face[0];
        v1 = face[1];
        v2 = face[2];
        v3 = face[3];
        /* Skip the face if any of its 4 corners failed the near-plane
           test. Per-face skip is much friendlier than discarding the
           whole cube the moment one corner crosses the camera plane. */
        if (!(valid[v0] & valid[v1] & valid[v2] & valid[v3])) continue;
        edge1x = px[v1] - px[v0];
        edge1y = py[v1] - py[v0];
        edge2x = px[v2] - px[v0];
        edge2y = py[v2] - py[v0];
        normalZ = (edge1x * edge2y) - (edge1y * edge2x);

        if (normalZ > 0) {
            fillTriangle(px[v0], py[v0], px[v1], py[v1], px[v2], py[v2], r, g, b);
            fillTriangle(px[v0], py[v0], px[v2], py[v2], px[v3], py[v3], r, g, b);
            //mrc_drawLine(px[v3], py[v3], px[v0], py[v0], 100, 100, 100);
        }
    }
}

/* Margin (Q16.16, ~2 blocks) behind the camera plane. Whole (x,*,z)
   columns whose yaw-rotated forward distance is past this margin
   contribute nothing to the visible scene, so we skip them before
   projecting any of their vertices. Per-cube vertex culling inside
   projectPoint_fx already drops individual vertices behind near-Z;
   doing the test here at column level avoids 8 fixed-point matrix
   multiplies + a clip per cube in the dropped column. */
#define BEHIND_MARGIN_FX   ((fx32)2 << 16)

void gameDraw(fx32 cosY_fx, fx32 sinY_fx, fx32 cosP_fx, fx32 sinP_fx,
              fx32 cam_x_fx, fx32 cam_y_fx, fx32 cam_z_fx) {
    int x, y, z;
    uint8 type;
    uint8 mask;
    fx32 dx_fx, dz_fx, rotZ_fx;
    for (x = 0; x < WORLD_SX; x++) {
        dx_fx = FX_FROM_INT(x) - cam_x_fx;
        for (z = 0; z < WORLD_SZ; z++) {
            dz_fx = FX_FROM_INT(z) - cam_z_fx;
            /* Yaw-rotated forward distance using the same convention
               as projectPoint_fx so visibility never disagrees with
               the projector. */
            rotZ_fx = -FX_MUL(dx_fx, sinY_fx) + FX_MUL(dz_fx, cosY_fx);
            if (rotZ_fx < -BEHIND_MARGIN_FX) continue;
            for (y = 0; y < WORLD_SY; y++) {
                type = world[x][y][z];
                if (type == BLOCK_AIR) continue;
                mask = visibleFaces[x][y][z];
                if (mask == 0) continue;
                drawCube((int32)x, (int32)y, (int32)z, (int)type, mask,
                         cosY_fx, sinY_fx, cosP_fx, sinP_fx,
                         cam_x_fx, cam_y_fx, cam_z_fx);
            }
        }
    }
}
void mrc_draw(int32 data)
{
    /* Pre-convert per-frame trig and camera position to Q16.16 once,
       so the per-vertex hot path inside drawCube/projectPoint_fx never
       touches software-float operations. */
    fx32 yaw_fx   = FX_FROM_FLOAT(-cam.yaw);
    fx32 pitch_fx = FX_FROM_FLOAT(-cam.pitch);
    fx32 cY_fx = fx_cos(yaw_fx);
    fx32 sY_fx = fx_sin(yaw_fx);
    fx32 cP_fx = fx_cos(pitch_fx);
    fx32 sP_fx = fx_sin(pitch_fx);
    fx32 cam_x_fx = FX_FROM_FLOAT(cam.pos.x);
    fx32 cam_y_fx = FX_FROM_FLOAT(cam.pos.y);
    fx32 cam_z_fx = FX_FROM_FLOAT(cam.pos.z);

    mrc_clearScreen(147, 180, 255);
    cam.yaw   += (float)diff_x * rot_speed;
    cam.pitch -= (float)diff_y * rot_speed;

    /* Per-frame counters & one-shot block actions. */
    g_frame_count++;
    if (g_action_break) { castRayAndAct(0); g_action_break = 0; }
    if (g_action_place) { castRayAndAct(1); g_action_place = 0; }
    gameDraw(cY_fx, sY_fx, cP_fx, sP_fx, cam_x_fx, cam_y_fx, cam_z_fx);
    /* Mario as a perspective-scaled billboard at marioPos.
       Walk-cycle: oscillate +/-MARIO_ANIM_RANGE around base X by sin().
       2.0 world units tall (= 2 blocks). Drawn after the world but
       before HUD so hand/GUI overlay him. */
    {
        float walk = my_sinf((float)g_frame_count * MARIO_ANIM_SPEED) * MARIO_ANIM_RANGE;
        drawSprite3D_fx(&marioBmp,
                        FX_FROM_FLOAT(marioPos.x + walk),
                        FX_FROM_FLOAT(marioPos.y),
                        FX_FROM_FLOAT(marioPos.z),
                        FX_FROM_INT(2),
                        cY_fx, sY_fx, cP_fx, sP_fx,
                        cam_x_fx, cam_y_fx, cam_z_fx);
    }
    gDrawBitmap(&handBmp, SCREEN_WIDTH - handBmp.width, SCREEN_HEIGHT - handBmp.height - 70);
    gDrawBitmapNt(&GUIBmp, SCREEN_WIDTH - GUIBmp.width, SCREEN_HEIGHT - GUIBmp.height);
    /* FPS HUD on top of everything. */
    drawFpsHud();
    mrc_refreshScreen(0,0,SCREEN_WIDTH,SCREEN_HEIGHT);
}
int32 mrc_init(void)
{
    globalTimer = mrc_timerCreate();
    initSinLUT();
    initSpriteData();
    gameStart();
    rebuildAllFaceMasks();
    /* 33 ms tick = 30 frames/sec target. */
    mrc_timerStart(globalTimer, 33, 0, mrc_draw, 1);
	return MR_SUCCESS;
}
