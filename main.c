/* ===== Q16.16 fixed-point math =================================== */
typedef int32 fx32;

/* The MTK / MRE SDK headers don't always typedef a 64-bit type, but
   the underlying ARM toolchain (RVCT / armcc / gcc) supports it as
   `long long`.  We need it for the multiply / divide intermediates. */
#ifndef MRE_HAS_INT64
typedef long long int64;
typedef unsigned long long uint64;
#define MRE_HAS_INT64 1
#endif

#define FX_SHIFT          16
#define FX_ONE            ((fx32)0x00010000)             /* 65536 */
#define FX_HALF           ((fx32)0x00008000)             /* 32768 */
#define FX_FROM_INT(i)    ((fx32)((int32)(i) << FX_SHIFT))
#define FX_TO_INT(f)      ((int32)((f) >> FX_SHIFT))
#define FX_FROM_FLOAT(f)  ((fx32)((float)(f) * 65536.0f))
#define FX_TO_FLOAT(f)    ((float)(f) * (1.0f / 65536.0f))

/* (a * b) >> 16 with 64-bit intermediate to avoid overflow.
   ARM926 has no native 64-bit multiply, but the compiler emits a
   reasonable __aeabi_lmul, which is still ~10x cheaper than a
   software-float multiply on a no-FPU core. */
#define FX_MUL(a, b) ((fx32)((((int64)(a)) * ((int64)(b))) >> FX_SHIFT))

/* (a << 16) / b, again with 64-bit intermediate. */
#define FX_DIV(a, b) ((fx32)((((int64)(a)) << FX_SHIFT) / (int64)(b)))

#define FX_NEAR_Z      ((fx32)(FX_HALF))                 /* 0.5 */
#define FX_FOCAL       ((fx32)(200 * FX_ONE))            /* 200.0 */
#define FX_SCREEN_HW   ((fx32)(130 * FX_ONE))            /* 130.0 */
#define FX_SCREEN_HH   ((fx32)(140 * FX_ONE))            /* 140.0 */

/* Q16.16 sine LUT, 1024 entries.  Index = (angle * 1024 / TWO_PI) mod 1024.
   Generated by gen_sin_LUT.py (with TABLE_SIZE = 1024). */
#define FX_SIN_LUT_SIZE 1024
#define FX_SIN_LUT_MASK 1023
extern const int32 fx_sin_lut[FX_SIN_LUT_SIZE];  /* defined at end of file */

/* (1024 / 2*PI) in Q16.16 for the angle->index conversion. */
#define FX_LUT_SCALE   ((fx32)10680707)  /* (1024 / (2*PI)) * 65536 */
/* PI/2 in Q16.16. */
#define FX_HALFPI      ((fx32)102944)

__inline fx32 fx_sin(fx32 angle_fx) {
    /* Convert fx-radians to LUT index, wrap with bitmask. */
    int32 idx = FX_MUL(angle_fx, FX_LUT_SCALE) >> FX_SHIFT;
    return fx_sin_lut[idx & FX_SIN_LUT_MASK];
}
__inline fx32 fx_cos(fx32 angle_fx) {
    return fx_sin(angle_fx + FX_HALFPI);
}
